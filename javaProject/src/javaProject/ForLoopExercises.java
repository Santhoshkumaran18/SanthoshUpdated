package javaProject;

public class ForLoopExercises {

	public static void main(String[] args) {
		for (int i = 1; i <= 9; i++) {
			if (i % 3 == 0) {
				System.out.println(i + " ");
			} else {
				System.out.print(i + " ");
			}

		}
		for (int j = 8; j <= 19; j++) {
			if (j % 3 == 1) {
				System.out.println(j + " ");
			} else {
				System.out.print(j + " ");
			}

		}


	}

}
